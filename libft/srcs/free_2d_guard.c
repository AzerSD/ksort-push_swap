#include "../includes/libft.h"

void	free_2d_guard(char ***arr)
{
	size_t	i;

	i = 0;
	if (*arr)
	{
		while (*(arr) && (*arr)[i])
		{
			free((*arr)[i]);
			(*arr)[i++] = NULL;
		}
		free(*arr);
		*arr = NULL;
	}
}
