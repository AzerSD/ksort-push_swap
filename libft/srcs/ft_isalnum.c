#include "../includes/libft.h"
#include <stdio.h>

bool	ft_isalnum(int32_t c)
{
	if ((c >= '0' && c <= '9') || (c >= 'a' && c <= 'z') || \
	(c >= 'A' && c <= 'Z'))
		return (true);
	return (false);
}
